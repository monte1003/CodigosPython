#numeros enteros: no tienen decimales, ni positivos ni negativos, se representan como int o long para largos.
#numeros reales: tienen decimales, tanto positivos como negativos, se representan con float
#numeros complejos: tienen parte imaginaria, se utilizan como complex
#strings: es una cadena de texto encerrada entre comillas dobles o simples, las representamos con string
#booleanos: puede tener dos valores: falso o verdadero, se usan en condicionales y ciclos, se reepresentan con bool.


