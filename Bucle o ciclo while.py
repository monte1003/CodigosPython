#el bucle o ciclo while es un ciclo que ejecuta una instrucción mientras se cumpla una determinada condición
#El programa ejecutara las instrucciones dentro del while mientras la condición se cumpla.
x= 0
while x<3:
    print(x)
    x+=1
print("Fin.")
